package labosi.LVpeti;

/**
 *
 * @author Bole
 */
public class Automobil {

    private String spremnik;
    private String potrošnja;
    private String vrsta_goriva;
    private String tip;
    private String marka;

    public Automobil() {
    }

    public String getSpremnik() {
        return spremnik;
    }

    public void setSpremnik(String spremnik) {
        this.spremnik = spremnik;
    }

    public String getPotrošnja() {
        return potrošnja;
    }

    public void setPotrošnja(String potrošnja) {
        this.potrošnja = potrošnja;
    }

    public String getVrsta_goriva() {
        return vrsta_goriva;
    }

    public void setVrsta_goriva(String vrsta_goriva) {
        this.vrsta_goriva = vrsta_goriva;
    }

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }

    public String getMarka() {
        return marka;
    }

    public void setMarka(String marka) {
        this.marka = marka;
    }

    
}
